<h1>Statuten des Rettungs-Corps der Stadt St. Gallen</h1>


<h2>Statuten</h2>
<p></p>
<p>Wo im folgenden männliche Personenbezeichnungen verwendet werden, sind darunter stets auch die entsprechenden weiblichen Bezeichnungen zu
    verstehen.</p>
<p></p>
<p></p>
<p><strong>1. Name und Zweck</strong></p>
<p></p>
<p>1.1 Unter dem Namen „Rettungs-Corps der Stadt St. Gallen“ (RC) besteht ein im Jahr 1859 gegründeter Verein gemäss Art. 60 ZGB. Sitz des
    Vereins ist St. Gallen</p>
<p></p>
<p>1.2 Der Verein ist gemäss Art. 61 ZGB im Handelsregister eingetragen.</p>
<p></p>
<p>1.3 Zweck des Vereins ist:</p>
<p></p>
<p>1.3.1 Pflege und Förderung der Kameradschaft unter den Aktiven und Mitgliedern der „Alten Garde“ (Altgardisten) sowie Durchführung
    repräsentativer Anlässe;</p>
<p></p>
<p>1.3.2 Verwaltung der Unterstützungskasse (UK), gemäss besonderem Reglement, welches Bestandteil dieser Statuten ist;</p>
<p></p>
<p>1.3.3 Unterhalt der Vereinshütte „Nördli“, gemäss besonderem Reglement, welches Bestandteil dieser Statuten ist.</p>
<p></p>
<p></p>
<p><strong>2. Mitgliedschaft</strong></p>
<p></p>
<p>2.1 Der Verein besteht aus:</p>
<p></p>
<p>2.1.1 Aktivmitgliedern</p>
<p></p>
<p>2.1.2 Altgardisten</p>
<p></p>
<p>2.2 Aktivmitglied kann auf schriftliches Eintrittsgesuch hin jede in der Feuerwehr der Stadt St. Gallen eingeteilte oder fest angestellte
    Person werden.</p>
<p></p>
<p>2.3 Die definitive Aufnahme erfolgt an der Hauptversammlung (HV) auf Antrag der Kommission. Jedes Mitglied wird in der Stammkontrolle
    eingetragen.</p>
<p></p>
<p>2.4 Der Rücktritt als Aktivmitglied erfolgt automatisch mit der Beendigung der Dienstleistung oder Anstellung in der Feuerwehr der Stadt
    St. Gallen oder auf schriftliches Gesuch hin.</p>
<p></p>
<p>2.5 Zum Altgardisten wird ernannt:</p>
<p></p>
<p>2.5.1 Wer die Feuerwehr – Dienstpflicht in der Stadt St. Gallen erfüllt hat und vom aktiven Dienst in allen Ehren zurückgetreten ist und
    mindestens die letzten 5 Jahre Mitglied des RC gewesen ist;</p>
<p></p>
<p>2.5.2 Wer als Angestellter der Berufsfeuerwehr in den Ruhestand tritt und mindestens die letzten 5 Jahre Mitglied des RC gewesen ist.</p>
<p></p>
<p>2.6 Zum Altgardisten kann ernannt werden, wer aus gesundheitlichen oder anderen wichtigen Gründen aus dem Feuerwehrdienst ausscheiden
    muss und entsprechend schriftlich Antrag an die Kommission stellt.</p>
<p></p>
<p>2.7 Anrecht auf ein Corps – Geschenk haben:</p>
<p></p>
<p>2.7.1 Angehörige der Pflichtfeuerwehr, die mindestens 20 Jahre Mitglied des RC waren;</p>
<p></p>
<p>2.7.2 Angehörige der Berufsfeuerwehr, die mindestens 20 Jahre Mitglied des RC waren und zu Altgardisten ernannt wurden.</p>
<p></p>
<p></p>
<h2>3. Recht und Pflichten</h2>
<p></p>
<p>3.1 Aktivmitglieder und Altgardisten besitzen gleiches Stimmrecht.</p>
<p></p>
<p>3.2 Gegen Beschlüsse der Kommission kann innert 14 Tagen nach Erhalt der Mitteilung schriftlich Rekurs an den Obmann, zuhanden der
    nächstfolgenden HV, erhoben werden. Diese entscheidet endgültig.</p>
<p></p>
<p>3.3 Jedes Mitglied verpflichtet sich, die Ehre und das Ansehen des RC durch Kameradschaft und Disziplin zu wahren und zu fördern.</p>
<p></p>
<p>3.4 Die Mitglieder entrichten einen Jahresbeitrag, der von der HV festgelegt wird. Der Beitrag kann für Aktive und Altgardisten
    verschieden sein.</p>
<p></p>
<p>3.5 Verfehlungen gegen bestehende Vorschriften und ungebührliches Verhalten Kameraden gegenüber werden, je nach Umständen, wie folgt
    geahndet: Mahnung durch den Obmann, Vorladung vor die Kommission, schriftliche Mahnung und Ultimatum. Rückfälligkeit, sowie grobe
    Verfehlungen ziehen den Ausschluss aus dem Verein nach sich.</p>
<p></p>
<p></p>
<h2>4. Ausschluss</h2>
<p></p>
<p>4.1 Die Kommission kann nach erfolgter Mahnung ein Mitglied, das sich gegenüber den Statuten stark verfehlt oder dem Ansehen des Vereins
    schadet, ausschliessen. Der Ausschluss muss schriftlich eröffnet werden. Der Ausgeschlossene hat das Rekursrecht an der nächsten HV.</p>
<p></p>
<p>4.2 Der Ausschluss entbindet nicht vom weiteren Feuerwehrdienst.</p>
<p></p>
<p>4.3 Mit dem Ausschluss erlischt jeder Anspruch auf das Vereinsvermögen.</p>
<p></p>
<p></p>
<h3>5. Organisation</h3>
<p></p>
<p>5.1 Die Organe des Vereins sind:</p>
<p></p>
<p>5.1.1 die <strong>HV</strong></p>
<p></p>
<p>5.1.2 die ausserordentlichen Versammlungen</p>
<p></p>
<p>5.1.3 die Kommission</p>
<p></p>
<p>5.1.4 der Ausschuss</p>
<p></p>
<p>5.1.5 die Geschäftsprüfungskommission (GPK)</p>
<p></p>
<p>5.2 Die HV findet in der Regel im ersten Kalenderquartal statt. Die Mitglieder werden schriftlich eingeladen.</p>
<p></p>
<p>5.3 Die ordentlichen Traktanden sind:</p>
<p></p>
<p>1. Appell und Festsetzen des absoluten Mehrs.</p>
<p></p>
<p>2. Wahl von Stimmenzähler</p>
<p></p>
<p>3. Protokoll der letzten HV</p>
<p></p>
<p>4. Jahresbericht des Obmannes</p>
<p></p>
<p>5. Rechnungsablagen</p>
<p></p>
<p>5.1 der UK</p>
<p></p>
<p>5.2 der Vereinskassen</p>
<p></p>
<p>5.3 der Kasse der Vereinshütte „Nördli“</p>
<p></p>
<ol>
    <li>Bericht der GPK über:</li>
</ol>
<p></p>
<p>6.1 die UK</p>
<p></p>
<p>6.2 die Vereinskassen</p>
<p></p>
<p>6.3 die Kasse der Vereinshütte „Nördli“</p>
<p></p>
<p>6.4 die Geschäftsführung</p>
<p></p>
<p>7. Festsetzen der Jahresbeiträge</p>
<p></p>
<p>8. Genehmigung des Beitrages der UK an die Vereinskasse</p>
<p></p>
<p>9. Mitgliederbewegungen</p>
<p></p>
<p>10. Wahlen</p>
<p></p>
<p>11. Tätigkeitsprogramm</p>
<p></p>
<p>12. Verschiedenes und Umfrage</p>
<p></p>
<p>5.4 Ausserordentliche Traktanden sind in der schriftlichen Einladung anzukündigen.</p>
<p></p>
<p>5.5 Ausserordentliche Versammlungen können einberufen werden entweder durch Beschluss der Kommission oder auf schriftlich begründetes
    Verlangen eines Fünftels der Mitglieder. Die Geschäfte sind in der schriftlichen Einladung anzukündigen.</p>
<p></p>
<p>5.6 Bei Wahlen und Abstimmungen entscheidet das absolute Mehr der anwesenden Mitglieder, bei allfälliger zweiter Abstimmung das relative
    Mehr.</p>
<p></p>
<p>5.7 Auf Verlangen eines Drittels der anwesenden Mitglieder kann geheime Abstimmung gefordert werden.</p>
<p></p>
<p>5.8 Die <strong>Kommission </strong>besteht aus mindestens 11 Mitgliedern</p>
<p></p>
<p>5.9 Die HV wählt:</p>
<p></p>
<p>5.9.1 den Obmann (Präsident), immer ein Aktivmitglied</p>
<p></p>
<p>5.9.2 den Verwalter der UK, immer ein Altgardist</p>
<p></p>
<p>5.9.3 mindestens 9 weitere Mitglieder, wobei mindestens 1 Mitglied der Hüttenkommission (Hüko) und nach Möglichkeit die Vertretung aller
    Einheiten und der Altgardisten zu gewährleisten ist.</p>
<p></p>
<p>5.9.4 Die HV wählt den Hüttenchef und die Hüttenwarte, diese bilden die Hüko</p>
<p></p>
<p>5.10 Die Kommission konstituiert sich selbst. Sie kann für besondere Aufgaben Arbeitsausschüsse bestellen, denen auch Personen angehören
    dürfen, die nicht Mitglied des Vereins sind.</p>
<p></p>
<p>5.11 Zu Kommissionssitzungen mit besonders wichtigen Traktanden ist die GPK einzuladen. Sie hat beratende Stimme.</p>
<p></p>
<p>5.12 Die Kommission ist beschlussfähig, wenn mindestens zwei Drittel der Mitglieder anwesend sind. Bei Abstimmungen entscheidet das
    relative Mehr; bei Stimmengleichheit hat der Obmann Stichentscheid.</p>
<p></p>
<p>5.13 Der Kommission obliegt im besonderen folgende Aufgaben:</p>
<p></p>
<p>5.13.1 Beaufsichtigung der UK gemäss UK-Reglement</p>
<p></p>
<p>5.13.2 Bestellung der dem RC zustehenden Abordnungen in Organisationen gemäss Art. 7.1 der Statuten</p>
<p></p>
<p>5.13.3 Aufnahme und Ausschluss von Mitgliedern</p>
<p></p>
<p>5.13.4 einmalige Ausgabenbeschlüsse bis höchstens Fr. 3‘000.-</p>
<p>jährlich wiederkehrende bis höchstens Fr. 500.-</p>
<p></p>
<p>5.13.5 Durchführung von Versammlungsbeschlüssen</p>
<p></p>
<p>5.14 Rücktrittsgesuche aus der Kommission haben schriftlich auf den 31. Dezember an den Obmann zu erfolgen.</p>
<p></p>
<p>5.15 Der <strong>Ausschuss</strong> besteht aus 5 Mitgliedern; dem Obmann, Obmann-Stellvertreter, UK-Verwalter, Kassier und
    Schriftführer. Er erledigt alle dringenden Angelegenheiten, sofern deren Wichtigkeit nicht die Einberufung der Kommission erfordert. In
    finanziellen Angelegenheiten hat er eine Ausgabenkompetenz von Fr. 1'500.- für den Einzelfall und Fr. 300.- für jährlich wiederkehrende
    Verpflichtungen.</p>
<p></p>
<p>5.16 Der Ausschuss ist berechtigt, Konversionen und Neuanlagen vorzunehmen und Delegationen zu bestellen. Er hat der Kommission Bericht
    zu erstatten.</p>
<p></p>
<p>5.17 Die <strong>GPK</strong> besteht aus 5 Mitgliedern und konstituiert sich selbst. Sie prüft die UK nach UK-Reglement. Ferner
    unterzieht sie die Vereinskassen einer gründlichen Prüfung. Die GPK legt der HV Bericht ab über die UK, die Vereinskassen, die Kasse der
    Vereinshütte „Nördli“ und die Geschäftsführung.</p>
<p></p>
<p>5.18 Der GPK steht das Recht zu, jederzeit Zwischenrevisionen vorzunehmen.</p>
<p></p>
<p>5.19 Sofern es die Kommission für notwendig erachtet, einen Bericht über die UK erstellen zu lassen, kann sie dazu auch einen externen
    Fachmann beauftragen. Dieser waltet seines Auftrages nach dem UK-Reglement.</p>
<p></p>
<p>5.20 Der <strong>Obmann</strong> leitet die Versammlungen, Kommissions- und Ausschuss-Sitzungen und trifft die im Interesse des Vereins
    notwendigen Anordnungen. Er überwacht die Einhaltung der Statuten und die Ausführungen der Beschlüsse der Vereinsorgane. Der Obmann
    vertritt das RC nach aussen. Er legt an der HV einen ausführlichen Bericht ab über das Vereinsjahr.</p>
<p></p>
<p>5.21 Der <strong>Obmann-Stellvertreter</strong> vertritt den Obmann bei Verhinderung und steht ihm in allen Angelegenheiten unterstützend
    bei. Er führt die Mitgliederkontrolle.</p>
<p></p>
<p>5.22 Der <strong>Verwalter der UK</strong> führt diese nach dem entsprechenden Reglement. Er schliesst die Rechnung auf den 31. Dezember
    ab. Für die ihm anvertrauten Gelder ist er persönlich haftbar. Die Kommission bestimmt das Honorar für seine Arbeit.</p>
<p></p>
<p>5.23 Der <strong>Kassier</strong> führt die Rechnung der Vereinskassen, die per 31. Dezember abzuschliessen sind. Für die ihm
    anvertrauten Gelder ist er persönlich haftbar.</p>
<p></p>
<p>5.24 Der <strong>Hüttenkassier</strong> führt die Buchhaltung der Vereinshütte „Nördli“, die per 31. Dezember abzuschliessen ist. Für die
    ihm anvertrauten Gelder ist er persönlich haftbar.</p>
<p></p>
<p>5.25 Der <strong>Schriftführer</strong> führt die Protokolle über sämtliche Versammlungen sowie Kommissions- und Ausschuss-Sitzungen und
    erledigt die ihm übertragene Korrespondenz.</p>
<p></p>
<p>5.26 Die rechtsverbindlichen Unterschriften für den Verein führen der Obmann, im Verhinderungsfall sein Stellvertreter, kollektiv mit dem
    Schriftführer, dem Kassier oder dem UK-Verwalter.</p>
<p></p>
<p>5.27 Das Geschäfts- und Vereinsjahr ist das Kalenderjahr.</p>
<p></p>
<p></p>
<h3>6. Kassen</h3>
<p></p>
<p>6.1 Es bestehen:</p>
<p></p>
<p>6.1.1 Vereinskassen (Corpskasse und zweckgebundene Kassen)</p>
<p></p>
<p>6.1.2 Die Unterstützungs-Kasse (UK), gemäss besonderem Reglement</p>
<p></p>
<p>6.1.3 Kasse der Vereinshütte „Nördli“ gemäss besonderem Reglement</p>
<p></p>
<p>6.2 Die Einnahmen der Kassen bestehen aus Beiträgen der UK und der Mitglieder, Schenkungen, Zinsen, Erlösen aus Veranstaltungen und
    verschiedenen Zuwendungen.</p>
<p></p>
<p>6.3 Die Ausgaben umfassen Aufwendungen für Verwaltung und Vereinsbedürfnisse, Leistungen aus Spezial- und Reisekasse, Kosten für
    Exkursionen, Delegationen und Verbandsbeiträge, soweit sie nicht durch die UK übernommen werden können.</p>
<p></p>
<p></p>
<h3>7. Verbände</h3>
<p></p>
<p>7.1 Das RC kann Körperschaften, welche die Förderung des Feuerwehrwesens zum Ziel haben oder einschlägige Fachgebiete bearbeiten, als
    Kollektivmitglied beitreten.</p>
<p></p>
<p></p>
<h3>8. Statutenrevisionen</h3>
<p></p>
<p>8.1 Eine Revision der Statuten, des UK-Reglements und des Reglements der Vereinshütte „Nördli“ kann an jeder Versammlung beantragt
    werden. Für das Eintreten ist eine Zweidrittelmehrheit der anwesenden Mitglieder notwendig. Der neue Statutenentwurf wird durch die
    Kommission oder hierzu Bevollmächtigte ausgearbeitet und der Versammlung vorgelegt. Die neuen Statuten treten erst in Kraft, wenn sie in
    einer nachfolgenden Versammlung mit Zweidrittelmehrheit der anwesenden Mitglieder genehmigt worden sind.</p>
<p></p>
<p>8.2 Sofern eine Teilrevision in der Einladung mit den betreffenden Artikeln angekündigt wurde, kann an derselben Versammlung sowohl der
    Beschluss auf Eintreten, als auch die Genehmigung erfolgen.</p>
<p></p>
<p></p>
<h3>9. Auflösung</h3>
<p></p>
<p>9.1 Für die Auflösung des RC ist der rechtsgültige Beschluss einer Versammlung und die Stimmenmehrheit von Zweidritteln aller Mitglieder
    erforderlich.</p>
<p></p>
<p>9.2 Im Falle einer Auflösung des Vereins gehen das Vermögen der UK, der Vereinskassen und der Kasse der Vereinshütte „Nördli“ samt dem
    Inventar zur sinngemässen Verwaltung an eine noch zu errichtende Stiftung mit der Verpflichtung, das Vermögen unter Wahrung der
    ursprünglichen Bestimmung der UK treuhänderisch zu verwalten und beschlossene Renten weiterhin auszurichten. Sollte die Stiftung, gemäss
    Art. 88 ZGB aufgelöst werden, wäre ein allfälliges Restvermögen karitativen Vereinen der Stadt St. Gallen zur Verfügung zu stellen.
    Sofern in der Stadt St. Gallen ein neuer Verein mit derselben Zielsetzung gegründet wird, ist die Stiftung aufzulösen und diesem Verein
    zur weiteren und bisherigen Nutzung zu übertragen.</p>
<p></p>
<p>10.1 Die Statuten vom 19. Dezember 1970 und sämtliche Nachträge werden aufgehoben.</p>
<p></p>
<p>10.2 Diese Statuten sind an der HV des Vereins vom 27. März 1992 genehmigt worden und treten sofort in Kraft.</p>
