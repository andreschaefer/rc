<h1>Hüttenreglement</h1>
<p></p>
<h3>1. Träger und Zweck</h3>
<p></p>
<p>1.1 Das RC führt gemäss Art. 1.3.3 der Statuten in separater Rechnung die Vereinshütte „Nördli“.</p>
<p></p>
<p>1.2 Das „Nördli“ steht den Mitgliedern des RC zu günstigen Konditionen zur Verfügung.</p>
<p></p>
<p>1.3 Die Vereinshütte „Nördli“ wird nach betriebswirtschaftlichen Grundsätzen geführt.</p>
<p></p>
<p></p>
<h3>2. Organisation</h3>
<p></p>
<p>2.1 Die Vereinshütte „Nördli“ untersteht der Aufsicht der ordentlichen Organe des RC gemäss Art. 5.1 der Statuten.</p>
<p></p>
<p>2.2.1 Der Hüttenchef erstattet an der HV Bericht über den Betrieb im verflossenen Jahr und der Hüttenkassier legt die Rechnung vor. Die
    Kommission setzt auf Antrag der Hüko eine jährliche Gesamtentschädigung fest.</p>
<p></p>
<p>2.2.2 Der Hüttenchef unterbreitet der Kommission das Ergebnis der Beratung der Hüko über bauliche und betriebliche Massnahmen sowie deren
    Kosten. Nach Genehmigung der Anträge ist deren rechtzeitige Durchführung sicherzustellen.</p>
<p></p>
<p>2.2.3 Die Hüttenkommission (Hüko) ist für die Verwaltung und Führung der Hütte verantwortlich.</p>
<p></p>
<p>2.3.1 Die Hüko führt Aufsicht und achtet auf Einhaltung der Hüttenordnung.</p>
<p></p>
<p>2.3.2 Sie sorgt für die abwechslungsweise Kontrolle der Hütte. Die Diensteinteilung für Ferien, Wochenende und Anlässe ist Sache des
    Hüttenchefs im Einvernehmen mit den Hüttenwarten.</p>
<p></p>
<p>2.3.3 Die Hüko hat für den Bestand eines zweckmässigen Inventars zu sorgen.</p>
<p></p>
<p>2.3.4 Die Hüko organisiert Anlässe und Arbeitstage nach Bedarf. Mitwirkende an Arbeitstagen erhalten Gratisverpflegung und kostenfreie
    Unterkunft.</p>
<p></p>
<p>2.4 Ungedeckte Kosten aus dem Betrieb der Vereinshütte „Nördli“ werden von der UK, gemäss Art. 3.2.3 (UK-Reglement) übernommen.</p>
<p></p>
<p></p>
<h3>3. Betrieb</h3>
<p></p>
<p>3.1.1 Die Benützer der Hütte entrichten eine Taxe gemäss Tarif.</p>
<p></p>
<p>3.1.2 Die Taxen sind in der Hütte gut sichtbar anzuschlagen.</p>
<p></p>
<p>3.1.3 Die Benützungstaxen sind durch die HV auf Antrag der Kommission jährlich festzulegen.</p>
<p></p>
<p>3.1.4 Von Nichtmitgliedern kann für Gruppen- und Ferienaufenthalte die Vorauszahlung verlangt werden.</p>
<p></p>
<p>3.1.5 Der diensthabende Hüttenwart mit der Familie ist taxfrei.</p>
<p></p>
<p>3.2.1 Die Benützer erhalten den Hüttenschlüssel nach erfolgter Anmeldung vom Hüttenchef oder dessen Stellvertreter.</p>
<p></p>
<p>3.2.2 Der persönliche Schlüssel des Hüttenwartes darf nicht weitergegeben werden.</p>
<p></p>
<p>3.2.3 Die Benützer geben die Hütte einem Hüttenwart ab.</p>
<p></p>
<p>3.3.1 Die Hüttenordnung sowie die Anordnung des diensttuenden Hüttenwartes sind von allen Besuchern zu beachten. Bei grober Widerhandlung
    kann der weitere Hüttenbesuch durch die Hüko verweigert werden.</p>
<p></p>
<p>3.4 Übernachtungen sind meldepflichtig. Reservationen sind beim Hüttenchef oder bei seinem Stellvertreter anzumelden.</p>
<p></p>
<p>3.5.1 Reklamationen und allfällige Beschädigungen sind sofort dem Hüttenchef zu melden.</p>
<p></p>
<p>3.5.2 Beschwerden gegen die Anordnungen der Hüttenwarte sind schriftlich direkt an den Ausschuss zu richten.</p>
<p></p>
<p>3.6.1 Das Inventar ist Eigentum des RC und darf nicht missbraucht, entfernt oder entwendet werden.</p>
<p></p>
<p>3.6.2 Die Inventarliste ist nachzutragen.</p>
<p></p>
<p></p>
<h3>4. Schlussbestimmungen</h3>
<p></p>
<p>4.1 Bei einer allfälligen Liquidation der Hütte ist es nur dem Hüttenchef gestattet, nach Anordnung der Kommission, das Inventar
    zweckmässig zu veräussern und darüber Rechnung abzulegen.</p>
<p></p>
<p>4.2 Das Hüttenreglement vom 20. März 1981 und sämtliche Nachträge werden aufgehoben.</p>
<p></p>
<p>4.3 Das vorliegende Reglement ist an der Hauptversammlung des Vereins vom 27. März 1992 genehmigt worden und tritt sofort in Kraft.</p>
