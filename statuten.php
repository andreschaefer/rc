<h1>Reglement über die Unterstützungskasse</h1>
<p></p>
<p></p>
<h3>1. Träger und Zweck</h3>
<p></p>
<p>1.1 Das RC führt gemäss Art. 1.3.2 der Statuten in separater Rechnung seine durch Vermächtnisse und anderen Zuwendungen geäufnete
    Unterstützungskasse (UK)</p>
<p></p>
<p>1.2 Die UK bezweckt, den Mitgliedern des RC Hilfe zu leisten.</p>
<p></p>
<p>1.3 Aus den Kapitalerträgnissen wird die Tätigkeit des RC gemäss den Statuten gefördert.</p>
<p></p>
<p></p>
<h3>2. Organisation</h3>
<p></p>
<p>2.1 Die UK untersteht der Aufsicht der ordentlichen Organe des RC gemäss Art. 5.1 der Statuten.</p>
<p></p>
<p>2.2 Die Hauptversammlung (HV) nimmt die Jahresrechnung ab und beschliesst die Beiträge der UK an die Vereinskassen.</p>
<p></p>
<p>2.3.1 Die Kommission überwacht die Kapitalanlagen.</p>
<p></p>
<p>2.3.2 Die Kommission beschliesst über Hilfeleistungen bis max. Fr. 1'500.- sowie die Gewährleistung von Überbrückungskrediten bis max.
    Fr. 12'000.-</p>
<p></p>
<p>2.4 Der Ausschuss nimmt auf Antrag des UK-Verwalters Kapitalanlagen und Konversionen vor. Bei Zeitnot kann der Verwalter im Einvernehmen
    mit dem Obmann oder dessen Stellvertreter handeln.</p>
<p></p>
<p>2.5 Der Ausschuss prüft die Gesuche und beschliesst deren Höhe unter Vorbehalt von Art. 2.3 des UK-Reglements</p>
<p></p>
<p>2.6 Der UK-Verwalter besorgt die fachgemässe Rechnungsführung und schliesst jährlich die Rechnung auf den 31. Dezember ab. Er stellt
    Antrag über Neuanlagen, Verkäufe, Konversionen und Entschädigungen. Ferner betreut er die Einleitung und Durchführung von
    Unterstützungsfällen und den Verkehr mit den Geldinstituten.</p>
<p></p>
<p>2.7 Die Geschäftsprüfungskommission (GPK) prüft die UK nach revisionstechnischen Grundsätzen und erstellt an der HV Bericht. Sie
    überwacht den Verkehr mit den Banken, die Veränderungen im offenen Depot der St. Gallischen Kantonalbank und den Kassenverkehr. Die
    Rechtmässigkeit von Ausgaben ist anhand der Protokolle zu überprüfen. Die GKP ist berechtigt, jederzeit eine Zwischenrevision und
    Kassensturz vorzunehmen.</p>
<p></p>
<p>2.8 Die rechtmässigen Unterschriften für die UK führen der Obmann,- im Verhinderungsfalle sein Stellvertreter – Kollektiv mit dem
    Verwalter oder dem Schriftenführer. Diese Unterschriften sind unmittelbar nach Ersatzwahlen bei den in Frage kommenden Banken und dem
    Handelsregisteramt zu hinterlegen.</p>
<p></p>
<p></p>
<h3>3. Vermögen</h3>
<p></p>
<p>3.1 Die Einnahmen bestehen aus den Kapitalerträgen, aus freiwilligen Beiträgen, Vermächtnissen und Zuwendungen.</p>
<p></p>
<p>3.2 Die Ausgaben umfassen:</p>
<p></p>
<p>3.2.1 Hilfeleistungen und Überbrückungskredite an die Mitglieder</p>
<p></p>
<p>3.2.2 Aufwendungen für spezielle Vereinsanlässe und Geschenke</p>
<p></p>
<p>3.2.3 Übernahme von ungedeckten Kosten aus dem Betrieb der Vereinshütte „Nördli“</p>
<p></p>
<p>3.2.4 Beiträge, Rückstellungen und Spenden</p>
<p></p>
<p>3.2.5 Entschädigungen an Verwalter und Revisoren</p>
<p></p>
<p>3.2.6 Bankgebühren, Büro- und Verwaltungskosten aus den Betrieb der UK</p>
<p></p>
<p>3.3.1 Das UK-Vermögen muss in schweizerischen Staats-, Kantons- und Gemeindeanleihen, in Anleihen solider schweizerischer Banken oder in
    Liegenschaften angelegt werden. Die Anlagen haben den Grundsätzen der Mündelsicherheit zu entsprechen.</p>
<p></p>
<p>3.3.2 Sämtliche Werttitel sind im offenen Depot bei der St. Galler Kantonalbank zu hinterlegen. Über alle hinterlegten Titel führt der
    UK-Verwalter eine Titelkontrolle und trägt Ein- und Ausgänge im offenen Depot laufend nach. Er ist ferner verantwortlich für die
    lückenlose Aufbewahrung der Bankabrechnungen und kontrolliert diese auf Richtigkeit.</p>
<p></p>
<p>3.3.3 Der UK-Verwalter ist dafür besorgt, dass freie Mittel laufend optimal zinstragend angelegt werden.</p>
<p></p>
<p>3.3.4 Darlehen dürfen nicht gewährt werden, vorbehalten bleibt Art. 2.3.2</p>
<p></p>
<p>3.4 Für die Verbindlichkeiten der UK haftet lediglich deren Vermögen. Eine persönliche Haftung der Mitglieder des RC ist
    ausgeschlossen.</p>
<p></p>
<p></p>
<h3>4. Leistungen der UK</h3>
<p></p>
<p>4.1 Erleidet ein Mitglied des RC bei offiziellen Anlässen Schaden, so kann die UK angemessenen Ersatz leisten, sofern nicht anderweitig
    eine Vergütung möglich ist. Der Schadenseintritt ist ausreichend zu beweisen und die beschädigten Gegenstände sind beizubringen. Die
    max. Entschädigung ist auf Fr. 1'500.- begrenzt.</p>
<p></p>
<p>4.2 Sofern sich ein Mitglied des RC oder nach dessen Tod seine nächsten Angehörigen in einer Notlage befinden, kann die UK gemäss Art.
    2.3.2 Hilfeleistungen oder Überbrückungskredite gewähren. Ein Rechtsanspruch auf solche Beiträge besteht nicht.</p>
<p></p>
<p>4.3 Gesuche um Unterstützung sind an den Obmann zu richten. Der Ausschuss kann von sich aus Schadenfälle und Notlagen abklären, wenn er
    von dritter Seite darauf aufmerksam gemacht wird. Die Gesuchsteller haben alle zur Abklärung erforderlichen Auskünfte zu erteilen und
    die nötigen Beweismittel beizubringen. Die Leistungspflicht der UK entfällt, wenn eingetretene Schadenereignisse nicht innert 30 Tagen
    zur Kenntnis des Obmannes gebracht werden.</p>
<p></p>
<p>4.4 Grundsätzlich werden die Leistungen an das Mitglied persönlich ausgerichtet. Der Ausschuss kann aber entscheiden, an wen und in
    welcher Form die beschlossenen Unterstützungen ausgerichtet werden. Er achtet auf die zweckmässige Verwendung der Mittel.</p>
<p></p>
<p>4.5 Die Ansprüche auf Leistungen der UK, sowie die ausgerichteten Gelder dürfen nicht verpfändet werden. Jede Abtretung von Ansprüchen an
    die UK ist ungültig.</p>
<p></p>
<p>4.6 Jede Anspruchsberechtigung an die UK erlischt mit dem Austritt oder dem Ausschluss aus dem RC, ebenso mit der Auflösung des
    Vereins.</p>
<p></p>
<p>4.7 Unterstützungen können unter folgenden Bedingungen ganz oder teilweise verweigert, eingestellt oder zurückgefordert werden:</p>
<p></p>
<p>4.7.1 Wenn durch die Leistungen anderer der normale Verdienst sichergestellt oder überschritten wird.</p>
<p></p>
<p>4.7.2 Wenn die UK zufolge Arglist, Täuschung, Unterdrückung von Tatsachen oder mangelhafter Auskünfte und Beweismittel zu Beiträgen
    veranlasst werden will oder bereits Leistungen erbracht hat.</p>
<p></p>
